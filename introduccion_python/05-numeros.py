# NÚMEROS
print(2)
print(4)
print(2.5)

# OPERACIONES (SUMA, RESTA, MULT, DIVISION)
print( 2 + 3 )
print( 8 - 4 )
print( 1 * 4 )
print( 10 / 3 )

# POTENCIAS DE UN NÚMERO
print( 4 ** 3 ) # 4*4*4 = 64
print( 3 ** 3 ) # 3*3*3 = 27

# OPERACIONES MÚLTIPLES
print ( (2 + 3) * 4 )

# INCREMENTACIONES
numero = 20
print(numero)
numero += 1
print(numero)
 
# SUMAS MENZCLANDO TIPOS DE NÚMEROS
print ( 4 + 2.9 )