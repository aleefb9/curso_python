lenguajes = ['Python', 'Kotlin', 'Java', 'JavaScript']

# bucle for (recorre toda la lista)
for lenguaje in lenguajes:
    print(lenguaje)

for lenguaje in lenguajes:
    print(f'estoy apendiendo {lenguaje}')

# buble de números en un rango
for numero in range(0, 10):
    print(numero)

# buble de números (de dos en dos)
for numero in range(0, 10, 2):
    print(numero)